char *rot13(char *p)
{
    int array1[] = {'A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J', 'K', 'L', 'M','a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm'};
    int array2[] = {'N', 'O', 'P', 'Q', 'R', 'S', 'T', 'U', 'V', 'W', 'X', 'Y', 'Z','n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'};
    int i;
    int a = 0;
    while (*(p +a) != '\0')
    {
        for (i = 0; i <= 26; i++)
        {
            if (*(p +a) == array1[i])
            *(p +a) = array2[i];
            else if (*(p +a) == array2[i])
            *(p +a) = array1[i];
        }
        a++;
    }
    return (p);
    
}

